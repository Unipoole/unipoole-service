--------------------------------------------------------
--  UNI_CONTENT_RELEASE
--------------------------------------------------------
SELECT * FROM (
SELECT COUNT(*) AS COUNT, CR.MODULE_ID, CR.ACTIVE
FROM UNI_CONTENT_RELEASE CR
WHERE CR.ACTIVE = 1
GROUP BY CR.MODULE_ID, CR.ACTIVE)
WHERE COUNT > 1;
