--------------------------------------------------------
--  UNI_TOOL_VERSION
--------------------------------------------------------
SELECT * FROM (
SELECT COUNT(*) AS COUNT, TV.DEVICE_REGISTRATION_ID, TV.TOOL_NAME, TV.ACTIVE 
FROM UNI_TOOL_VERSION TV
WHERE TV.ACTIVE = 1
GROUP BY TV.DEVICE_REGISTRATION_ID, TV.TOOL_NAME, TV.ACTIVE)
WHERE COUNT > 1;

