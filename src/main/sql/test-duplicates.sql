--------------------------------------------------------
--  UNI_CONTENT_VERSION
--------------------------------------------------------
SELECT * FROM (
SELECT COUNT(*) AS COUNT, CV.MODULE_REGISTRATION_ID, CV.TOOL_NAME, CV.ACTIVE 
FROM UNI_CONTENT_VERSION CV
WHERE CV.ACTIVE = 1
GROUP BY CV.MODULE_REGISTRATION_ID, CV.TOOL_NAME, CV.ACTIVE)
WHERE COUNT > 1;

